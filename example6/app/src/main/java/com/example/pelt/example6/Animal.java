package com.example.pelt.example6;

public class Animal {

    private int id;
    private int imageRes;

    public Animal(int id, int imageRes) {
        this.id = id;
        this.imageRes = imageRes;
    }

    public int getId() {
        return id;
    }

    public int getImageRes() {
        return imageRes;
    }

}
